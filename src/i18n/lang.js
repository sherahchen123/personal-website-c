// 获取翻译文本的辅助函数
export const getText = (key, language = 'zh') => {
  const keys = key.split('.');
  let value = messages[language];
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return key; // 如果找不到翻译，返回原键名
    }
  }
  
  return value;
};